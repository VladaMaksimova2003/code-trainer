def main() -> int:
    repo = Path(__file__).resolve().parents[2]
    if not (repo / "docs").is_dir() and Path("/app/docs").is_dir():
        repo = Path("/app")
    out = repo / "docs" / "python_course_v1_catalog.md"
    out.write_text(generate(), encoding="utf-8")
    core = len(_all_core_tasks())
    print(f"Wrote {out} ({core} core + {len(BLOCK_CAPSTONES)} capstone)")
    return 0


