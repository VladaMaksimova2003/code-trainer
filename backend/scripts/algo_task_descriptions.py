def is_generic_description(detail: str, title: str) -> bool:
    text = str(detail or "").strip()
    if not text or text in {title, f"{title}."}:
        return True
    return any(marker in text for marker in GENERIC_MARKERS)


def enrich_detailed_description(task: dict) -> None:
    num = int(task.get("task_num") or 0)
    title = str(task.get("title") or "").strip()
    detail = str(task.get("detailed_description") or "").strip()
    if num not in DETAILED_BY_NUM:
        return
    canonical = DETAILED_BY_NUM[num]
    if is_generic_description(detail, title) or detail != canonical:
        task["detailed_description"] = canonical
        short = str(task.get("short_goal") or "").strip().rstrip(".")
        if not short or short.lower() == title.lower() or is_generic_description(short, title):
            task["short_goal"] = canonical.split(".")[0].strip() + "."
